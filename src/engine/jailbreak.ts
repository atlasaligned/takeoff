export type JbSeverity = 'minor' | 'bad' | 'severe' | 'terminal';

export function jbSeverity(capability: number): JbSeverity {
  if (capability < BAL.JB_MINOR_MAX) return 'minor';
  if (capability < BAL.JB_BAD_MAX) return 'bad';
  if (capability < BAL.JB_SEVERE_MAX) return 'severe';
  return 'terminal';
}

/** Weekly probability the lab's flagship gets jailbroken. */
export function jailbreakChance(state: GameState, lab: Lab): number {
  const m = flagship(lab);
  if (!m) return 0;
  const capTerm = Math.pow(Math.max(0, m.capability), BAL.JB_CAP_EXP);
  const adoptTerm = 0.5 + state.world.adoption / 100;
  const robustTerm = Math.pow(Math.max(0, 100 - m.robustness) / 70, BAL.JB_ROBUST_EXP);
  return clamp(BAL.JB_BASE * capTerm * adoptTerm * robustTerm, 0, 0.5);
}

export interface JbOutcome {
  name: string;
  severity: JbSeverity;
  description: string;
  /** true if the game ends for everyone */
  terminal: boolean;
  /** true if corrigibility core saved the world at the last second */
  saved: boolean;
  /** true if a research counter flipped the event into good PR */
  countered: boolean;
}

interface JbDef {
  name: string;
  description: string;
  /** extra availability condition */
  when?: (state: GameState, lab: Lab) => boolean;
  /** bio events grow more severe once the lab has Self-Driving Labs */
  bio?: boolean;
  apply: (state: GameState, lab: Lab, damp: number) => void;
}

function hitTrust(lab: Lab, pub: number, gov: number, damp: number): void {
  lab.publicTrust = clamp100(lab.publicTrust - pub * damp);
  lab.govTrust = clamp100(lab.govTrust - gov * damp);
}

function spikeRiskFear(state: GameState, amount: number, damp: number): void {
  for (const g of Object.values(state.govs)) g.riskFear = clamp(g.riskFear + amount * damp, BAL.FEAR_FLOOR, 100);
}

