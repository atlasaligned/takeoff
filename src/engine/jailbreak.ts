const TABLES: Record<JbSeverity, JbDef[]> = { minor: MINOR, bad: BAD, severe: SEVERE, terminal: TERMINAL };

/** Roll and, if unlucky, apply a jailbreak for this lab. Returns outcome or null. */
export function jailbreakTick(state: GameState, lab: Lab, rng: RngState): JbOutcome | null {
  const m = flagship(lab);
  if (!m) return null;
  const p = jailbreakChance(state, lab);
  if (!chance(rng, p)) return null;

  const severity = jbSeverity(m.capability);
  const pool = TABLES[severity].filter((def) => !def.when || def.when(state, lab));
  if (pool.length === 0) return null;
  const def = pick(rng, pool);
  let damp = state.diplomacy.completed.includes('crisis-hotline') ? BAL.RED_PHONE_DAMPING : 1;
  // Self-Driving Labs makes every biological incident deadlier
  if (def.bio && hasR(lab, 'self-driving-labs')) damp *= 1.5;

  if (severity === 'terminal') {
    // Universal Vaccines are a hard counter to any bio-terminal event
    if (def.bio && hasR(lab, 'universal-vaccines')) {
      lab.publicTrust = clamp100(lab.publicTrust + 8);
      lab.govTrust = clamp100(lab.govTrust + 10);
      spikeRiskFear(state, 12, 1);
      return { name: def.name, severity, description: def.description, terminal: false, saved: true, countered: true };
    }
    // Corrigibility Core and AI Control Protocols each give an independent chance to prevent it
    const share = lab.chips > 0 ? lab.alloc.alignment / lab.chips : 0;
    const pCorr = hasR(lab, 'corrigibility') ? BAL.CORRIGIBILITY_SAVE : 0;
    const pControl = hasR(lab, 'ai-control') ? Math.min(BAL.AI_CONTROL_SAVE_CAP, share * BAL.AI_CONTROL_SHARE_SCALE) : 0;
    const pSave = 1 - (1 - pCorr) * (1 - pControl);
    if (pSave > 0 && chance(rng, pSave)) {
      // last-second shutdown; the world learns how close it was
      hitTrust(lab, 15, 10, damp);
      spikeRiskFear(state, 20, damp);
      return { name: def.name, severity, description: def.description, terminal: false, saved: true, countered: false };
    }
    return { name: def.name, severity, description: def.description, terminal: true, saved: false, countered: false };
  }

  const countered = def.name === 'Engineered Pathogen' && hasR(lab, 'universal-vaccines');
  def.apply(state, lab, damp);
  return { name: def.name, severity, description: def.description, terminal: false, saved: false, countered };
}
